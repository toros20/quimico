                     <h2>Fondo de Auxilio Mutuo</h2>
                    
                    Desde un principio existió la aspiración gremial de tener para cada profesional un fondo de seguro de vida por muerte natural y por accidente; un seguro de jubilación y retiro; un seguro médico.  En aquel tiempo, los asegurados apenas llegaban alrededor de 200 a 300 colegiados, comparado con los incorporados a la fecha, se tienen ya 3,644 miembros del Colegio (10 a 12 veces más).
                    Este siempre fue un tema sumamente delicado porque se trataba de manejar dinero ajeno y todo mundo sabe que llega el momento de jubilación en que se llegaría a necesitar para  el soporte  de vida de los profesionales jubilados.
                    Se decidió en aquel tiempo por constituir un Fondo de Auxilio Mutuo,  porque se consideraba que el profesional podría llegar en un momento a preferir el uso de su seguro para uso propio por enfermedad u otra necesidad, en lugar de heredarlo a su familia.  Para el seguro de accidentes, en aquel tiempo se contrató una Compañía de Seguros comprando un seguro colectivo de accidentes. Todo esto funcionó bastante bien durante muchos años.
                    A medida que iba aumentando el número de colegiados, iba engrosando las arcas específicas del Fondo de Auxilio Mutuo.  Se insistió desde un principio en contabilidades separadas, registros de los aportes del asegurado y algunos aportes del Colegio. Se hacía uso de análisis y estudios actuariales y muy estricta auditoría de los Fondos, informes que la Tesorería presentaba también en las Asambleas Anuales. La adecuada inversión de los Fondos, lógicamente debería haber requerido  el uso de Fideicomiso a través de Fiduciarios que se encargaran de una sana administración financiera en vez de hacerlo en simples depósitos bancarios. Esto principalmente por el atractivo de las inversiones en cuanto a las tasas de interés que muchas veces son elevadas pero con un alto riesgo, aspectos que las entidades fiduciarias especializadas lo pueden medir con mayor facilidad y equidad.

